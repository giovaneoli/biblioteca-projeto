    private void confirmarDevolucaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmarDevolucaoActionPerformed
        
        Double totalMul = Double.parseDouble(totalMulta.getText().replace(",","."));
        Double valPago = Double.parseDouble(valorPago.getText().replace(",","."));
     
            if (TelaRegistrarDevolucaoController.registrarDevolucao(idEmprestimo.getText())) { 
                Component frame = null;
                JOptionPane.showMessageDialog(frame, "Devolução registrada com sucesso!");
            }
        if (totalMul > 0){
             if (Objects.equals(valPago, totalMul)){
            Component frame = null;
            JOptionPane.showMessageDialog(frame, "Multa paga!");
        }
        else {
            Double valDevedor = totalMul - valPago;
            String valor = String.format("%.2f", valDevedor);
            valor = valor.replace(",",".");
            if (valDevedor<0){
                Component frame = null;
                JOptionPane.showMessageDialog(frame, "Multa paga!");
            }
            else if (TelaRegistrarDevolucaoController.bloquearUsuario(idEmprestimo.getText(), idUsuario.getText(), 
                   valor, descricaoMulta.getText())) {                
                Component frame = null;
                JOptionPane.showMessageDialog(frame, "Usuário bloqueado por falta de pagamento!");   
            }
        }
        }
       
        
        TelaEmprestimos tela = new TelaEmprestimos();
        tela.setVisible(true);
        dispose();
    }//GEN-LAST:event_confirmarDevolucaoActionPerformed

