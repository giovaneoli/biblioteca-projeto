    public  TelaRegistrarDevolucao(String id) {
        initComponents();
        formatarData ();
        
        idEmprestimo.setVisible(false);
        idEmprestimo.setText(id);
        
        ArrayList<String> dados= new ArrayList<>();
        dados= TelaRegistrarDevolucaoController.dadosEmprestimo(id);  
        idUsuario.setVisible(false);
        idUsuario.setText(dados.get(1));
        usuario.setText(dados.get(2));
        livro.setText(dados.get(3));
        String editaData= dados.get(4);
        int ano = Integer.parseInt(editaData.substring(0,4));
        int mes = Integer.parseInt(editaData.substring(5,7));
        int dia = Integer.parseInt(editaData.substring(8,10));
        editaData = editaData.substring(8,10) + editaData.substring(5,7) + editaData.substring(0,4);
        dataDevolucao.setText(editaData);
        valorPago.setText("0,00");
        totalMulta.setText("0,00");
        
        LocalDateTime dataCadastro = LocalDateTime.of(ano,mes,dia,0,0);
        LocalDateTime hoje = LocalDateTime.now();

        //calcula diferença de dias
        long dias = ChronoUnit.DAYS.between(dataCadastro, hoje);
        
        String editaHoje = hoje.toString().substring(0, 10);
        editaHoje = editaHoje.substring(8,10) + "/" + editaHoje.substring(5,7) + "/" + editaHoje.substring(0,4);
        dataAtual.setText(editaHoje);
        
        double calcMultaAtraso = 0;
        if (dias > 0){
            calcMultaAtraso = dias * 0.15;
            
            
            multaAtraso.setText(String.format("%.2f", calcMultaAtraso));
            totalMulta.setText(String.format("%.2f", calcMultaAtraso));
        }
        else {
            multaAtraso.setText("0.00");
        }
        dataDevolucao.setEditable(false);
        usuario.setEditable(false);
        livro.setEditable(false);
        dataDevolucao.setEditable(false);
        multaAtraso.setEditable(false);
        totalMulta.setEditable(false);
        descricaoMulta.setEditable(false);
        valorPago.setText("0,00");
        if (calcMultaAtraso > 0){
            valorPago.setEditable(true);
        }
        else {
            valorPago.setEditable(false);
        }
        
    }

    private void formatarData () {
        try {
            MaskFormatter mask = new MaskFormatter ("##/##/####");
            mask.install(dataAtual);
            mask.install(dataDevolucao);
            
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao formatar campo de texto", "Erro", JOptionPane.ERROR);
        }
 }

