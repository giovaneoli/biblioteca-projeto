    //Atualiza valor total da multa, conforme valor da multa por deterioração
    private void multaDeterioracaoKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_multaDeterioracaoKeyReleased
        if (multaDeterioracao.getText().length()>0){
            Double multaDet = Double.parseDouble(multaDeterioracao.getText().replace(",","."));
              Double somaFinal = multaDet + Double.parseDouble(multaAtraso.getText().replace(",","."));
              totalMulta.setText(String.format("%.2f", somaFinal));
              descricaoMulta.setEditable(true);
              valorPago.setEditable(true);
        }
              
    }//GEN-LAST:event_multaDeterioracaoKeyReleased

