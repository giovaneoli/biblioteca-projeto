    //Abre a edição de cadastro e fecha a tela atual
    private void editarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarActionPerformed
        try {
            int linhaSelecionada = tabelaUsuarios.getSelectedRow();
            String id = tabelaUsuarios.getValueAt(linhaSelecionada,0).toString();
            EditarCadastroDeUsuários tela = new EditarCadastroDeUsuários(id);
            tela.setVisible(true);
            dispose();
        }
       
        catch (ArrayIndexOutOfBoundsException exception){
                    
            JOptionPane.showMessageDialog(null, "Selecione um cadastro para editar!", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_editarActionPerformed

