    //Confirma a solicitação de exclusão e exclui
    private void excluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirActionPerformed
    try {
        UIManager.put("OptionPane.noButtonText", "Não");
        UIManager.put("OptionPane.yesButtonText", "Sim");
        int linhaSelecionada = tabelaUsuarios.getSelectedRow();
        String id = tabelaUsuarios.getValueAt(linhaSelecionada, 0).toString();
        int dialogButton = 0;
        int dialogResult = JOptionPane.showConfirmDialog(null, "Você tem certeza que deseja excluir?", "ATENÇÃO!", dialogButton);
        if (dialogResult == JOptionPane.YES_OPTION) {

        
        TelaUsuariosCadastradosController.deleteUsuario(id);
        TelaUsuariosCadastrados tela = new TelaUsuariosCadastrados();
            tela.setVisible(true);
            dispose();
        }
    }
       
        catch (ArrayIndexOutOfBoundsException exception){
                    
            JOptionPane.showMessageDialog(null, "Selecione um cadastro para excluir!", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_excluirActionPerformed

    //Abre a tela de desbloqueio - se o usuário estiver bloqueado - e fecha a atual
    private void desbloquearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_desbloquearActionPerformed
        try {
            int linhaSelecionada = tabelaUsuarios.getSelectedRow();
            String id = tabelaUsuarios.getValueAt(linhaSelecionada,0).toString();
            String status = tabelaUsuarios.getValueAt(linhaSelecionada,2).toString();
            if (status.contentEquals("bloqueado")){
                TelaDesbloquearUsuario tela = new TelaDesbloquearUsuario(id);
                tela.setVisible(true);
                dispose();
            }
            else {
                Component frame = null;
                JOptionPane.showMessageDialog(frame, "Este usuário não está bloqueado!");
            }
        }
       
        catch (ArrayIndexOutOfBoundsException exception){
                    
            JOptionPane.showMessageDialog(null, "Selecione um cadastro para desbloquear!", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_desbloquearActionPerformed

