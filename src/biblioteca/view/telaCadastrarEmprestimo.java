    // Cadstra o empréstimo
    private void cadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastrarActionPerformed
        int linhaSelecionada = tabelaLivros.getSelectedRow();
        String idLivro = tabelaLivros.getValueAt(linhaSelecionada, 0).toString();
        
        int linhaSelecionada2 = tabelaUsuarios.getSelectedRow();
        String idUsuario = tabelaUsuarios.getValueAt(linhaSelecionada2, 0).toString();
        
        String editaData= dataDevolucao.getText();
        int ano = Integer.parseInt(editaData.substring(6,10));
        int mes = Integer.parseInt(editaData.substring(3,5));
        int dia = Integer.parseInt(editaData.substring(0,2));
        LocalDateTime dataCadastro = LocalDateTime.of(ano,mes,dia,0,0);
        LocalDateTime hoje = LocalDateTime.now();
        long dias = ChronoUnit.DAYS.between(hoje, dataCadastro);
        
        if (dias < 6){
            System.out.println("dias = " + dias);
            JOptionPane.showMessageDialog(null, "A data de devolução deve ser de pelo menos 7 dias após o empréstimo!", "Erro", JOptionPane.ERROR_MESSAGE);
        }
        else {
            if(telaCadastrarEmprestimoController.registrar(idLivro, idUsuario, dataEmprestimo.getText(), 
                dataDevolucao.getText())){
            Component frame = null;
            JOptionPane.showMessageDialog(frame, "Empréstimo cadastrado com sucesso!");
         
            TelaEmprestimos tela = new TelaEmprestimos();
            tela.setVisible(true);
            dispose();
            
            }  
        }
        
        //Solicita cadastro de empréstimo
         
    }//GEN-LAST:event_cadastrarActionPerformed

