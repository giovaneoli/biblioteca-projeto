    //Atualiza tabela dinamicamente
    private void buscaUsuarioKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_buscaUsuarioKeyReleased
       //Apaga todos os dados da tabela
       tabelaUsuarios.removeAll();
       //Pesquisa novamente com base no texto digitado pelo usuário e preenche a tabela
       ArrayList<String> dados = telaCadastrarEmprestimoController.buscaUsuarios(buscaUsuario.getText());
        
            DefaultTableModel model =(DefaultTableModel) tabelaUsuarios.getModel();
            model.setNumRows(0);

            if (dados.size() > 1){
                for (int i=0; i<dados.size();){
                    model.addRow(new Object[] { 

                        dados.get(i), 
                        dados.get(i+1), 
                        dados.get(i+2)});
                    i=i+3;
                }
            }
            else {
            tabelaUsuarios.removeAll();
        }
    }//GEN-LAST:event_buscaUsuarioKeyReleased

