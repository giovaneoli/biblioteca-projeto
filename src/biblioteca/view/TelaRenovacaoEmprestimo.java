    //carrega os dados do empréstimo para renovação
    public TelaRenovacaoEmprestimo(String id) {
        initComponents();
        formatarData();
        ArrayList<String> dados= new ArrayList<>();
        dados = TelaRenovacaoEmprestimosController.dadosEmprestimo(id);
        idEmprestimo.setText(dados.get(0));
        titulo.setText(dados.get(1));
        renovacaoNumero.setText(dados.get(2));
        int renovacao = Integer.parseInt(dados.get(2));
        String editaData = dados.get(3);
        editaData = editaData.substring(8,10) + editaData.substring(5,7) + editaData.substring(0,4);
        dataDevolucaoAnterior.setText(editaData);
        idEmprestimo.setEditable(false);
        titulo.setEditable(false);
        renovacaoNumero.setEditable(false);
        dataDevolucaoAnterior.setEditable(false); 
        
        if (renovacao == 3){
            Component frame = null;
            JOptionPane.showMessageDialog(frame, "Já renovado 3 vezes, não é possível renovar!");
            novaDataDevolucao.setEditable(false);
            renovar.setEnabled(false);
        }
        
    }
    
    private void formatarData () {
        try {
            MaskFormatter mask = new MaskFormatter ("##/##/####");
            mask.install(dataDevolucaoAnterior);
            MaskFormatter mask2 = new MaskFormatter ("##/##/####");
            mask2.install(novaDataDevolucao);
            
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao formatar campo de texto", "Erro", JOptionPane.ERROR);
        }
 }

