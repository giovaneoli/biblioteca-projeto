    //Tenta realizar a renovelão, exibe mensagem de sucesso, abre tela
    //Gerenciar empréstimos e fecha a atual
    private void renovarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_renovarActionPerformed
       
        String editaData= dataDevolucaoAnterior.getText();
        int ano = Integer.parseInt(editaData.substring(6,10));
        int mes = Integer.parseInt(editaData.substring(3,5));
        int dia = Integer.parseInt(editaData.substring(0,2));
        LocalDateTime devAnterior = LocalDateTime.of(ano,mes,dia,0,0);
        editaData= novaDataDevolucao.getText();
        ano = Integer.parseInt(editaData.substring(6,10));
        mes = Integer.parseInt(editaData.substring(3,5));
        dia = Integer.parseInt(editaData.substring(0,2));
        LocalDateTime devNova = LocalDateTime.of(ano,mes,dia,0,0);
        long dias = ChronoUnit.DAYS.between(devAnterior, devNova);
        
        if (dias < 6){
            System.out.println("dias = " + dias);
            JOptionPane.showMessageDialog(null, "A data de renovação deve ser de pelo menos 7 dias após o empréstimo!", "Erro", JOptionPane.ERROR_MESSAGE);
        }
        else if (TelaRenovacaoEmprestimosController.gravarRenovacao(idEmprestimo.getText(),
                renovacaoNumero.getText(), novaDataDevolucao.getText())) {
            
            Component frame = null;
            JOptionPane.showMessageDialog(frame, "Renovação cadastrada com sucesso!");
            
            TelaEmprestimos tela = new TelaEmprestimos();
            tela.setVisible(true);
            dispose();
            
            }
    }//GEN-LAST:event_renovarActionPerformed

