    //Questiona a exclusÃ£o do cadastro e a realiza (ou interrompe)
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            UIManager.put("OptionPane.noButtonText", "NÃ£o");
            UIManager.put("OptionPane.yesButtonText", "Sim");
            int linhaSelecionada = tabelaLivros.getSelectedRow();
            String id = tabelaLivros.getValueAt(linhaSelecionada, 0).toString();
            int dialogButton = 0;
            int dialogResult = JOptionPane.showConfirmDialog(null, "VocÃª tem certeza que deseja excluir?", "ATENÃÃO!", dialogButton);
            if (dialogResult == JOptionPane.YES_OPTION) {
            TelaLivrosCadastradosController.deleteLivros(id);
            TelaLivrosCadastrados tela = new TelaLivrosCadastrados();
            tela.setVisible(true);
            dispose();
            }
        }
       
        catch (ArrayIndexOutOfBoundsException exception){
                    
            JOptionPane.showMessageDialog(null, "Selecione um cadastro para excluir!", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed
 
