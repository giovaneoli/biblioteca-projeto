    public EditarCadastroDeUsuários(String id) {
        initComponents();
        formatarData();
        formatarRG();
        formatarCPF();
        formatarCEP();
        ArrayList<String> dados= new ArrayList<>();
        dados= EditarCadastroDeUsuariosController.dadosUsuario(id);
        idUsuario.setText(dados.get(0));
        nome.setText(dados.get(1));
        rua.setText(dados.get(2));
        numero.setText(dados.get(3));
        bairro.setText(dados.get(4));
        cidade.setText(dados.get(5));
        estado.setSelectedItem(dados.get(6)); //arrumar
        telefone.setText(dados.get(7));
        rg.setText(dados.get(8));
        cpf.setText(dados.get(9));
        String editaData= dados.get(10);
        editaData = editaData.substring(8,10) + editaData.substring(5,7) + editaData.substring(0,4);
        dataNascimento.setText(editaData);
        cep.setText(dados.get(11));
        idUsuario.setEditable(false);
    }
    
    private void formatarCEP() {
        try {
            MaskFormatter mask = new MaskFormatter ("##.###-###");
            mask.install(cep);
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao formatar campo de texto", "Erro", JOptionPane.ERROR);
        }
    }
    private void formatarRG () {
        try {
            MaskFormatter mask = new MaskFormatter ("##.###.###-#");
            mask.install(rg);
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao formatar campo de texto", "Erro", JOptionPane.ERROR);
        }
 }
    private void formatarCPF () {
        try {
            MaskFormatter mask = new MaskFormatter ("###.###.###-##");
            mask.install(cpf);
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao formatar campo de texto", "Erro", JOptionPane.ERROR);
        }
 }
 private void formatarData () {
        try {
            MaskFormatter mask = new MaskFormatter ("##/##/####");
            mask.install(dataNascimento);
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao formatar campo de texto", "Erro", JOptionPane.ERROR);
        }
 }
