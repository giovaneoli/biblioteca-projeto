    //Salva as alterações, exibe mensagem, volta para a tela anterior e fecha atual
    private void cadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastrarActionPerformed

        if (EditarCadastroDeUsuariosController.editarDados(idUsuario.getText(), nome.getText(), rg.getText(), cpf.getText(), telefone.getText(),
                    rua.getText(), numero.getText(), bairro.getText(), cidade.getText(), dataNascimento.getText(),                   
                    estado.getSelectedItem().toString(), cep.getText())) {

           Component frame = null;
           JOptionPane.showMessageDialog(frame, "Dados alterados com sucesso!");

           TelaUsuariosCadastrados Tela = new TelaUsuariosCadastrados();
           Tela.setVisible(true);
           dispose();
           }  
    }//GEN-LAST:event_cadastrarActionPerformed

